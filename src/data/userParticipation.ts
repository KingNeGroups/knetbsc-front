// Function to get user participation data by address
export function getUserParticipation(address: string): UserParticipation | null {
  return userParticipationData[address.toLowerCase()] || null;
}

// Function to get all participants sorted by amount
export function getAllParticipants(): UserParticipation[] {
  return Object.values(userParticipationData).sort((a, b) => b.knetAmount - a.knetAmount);
}

// Function to get total participation
export function getTotalParticipation(): { totalKnet: number; totalFloa: number } {
  const participants = Object.values(userParticipationData);
  const totalKnet = participants.reduce((sum, p) => sum + p.knetAmount, 0);
  const totalFloa = participants.reduce((sum, p) => sum + p.floaAmount, 0);
  return { totalKnet, totalFloa };
}